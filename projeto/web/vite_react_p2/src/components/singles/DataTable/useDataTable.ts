import {
  getCoreRowModel,
  getFilteredRowModel,
  getPaginationRowModel,
  getSortedRowModel,
  useReactTable,
  type ColumnFiltersState,
  type SortingState,
} from '@tanstack/react-table';
import { useEffect, useState } from 'react';
import { defaultClassNames } from './data-table.styles';
import type { DataTableProps } from './interfaces/DataTableProps';

export default function useDataTable<TData>({
  data,
  columns,
  emptyMessage = 'Nenhum dado encontrado',
  showPagination = true,
  isLoading = false,
  onRowClick,
  classNames,
  datatableServerSideManager,
}: DataTableProps<TData>) {
  const { filters = [], onFiltersChange } = datatableServerSideManager?.where || {};
  const { sortField, onSortChange, sortDirection } = datatableServerSideManager?.ordernation || {};
  const {
    totalItems,
    currentPage,
    pageSize = 20,
    pageSizeOptions = [10, 20, 30, 50],
    onPageChange,
    onPageSizeChange,
  } = datatableServerSideManager?.pagination || {};

  const [sorting, setSorting] = useState<SortingState>([]);
  const [columnFilters, setColumnFilters] = useState<ColumnFiltersState>([]);
  const [goToPage, setGoToPage] = useState('');

  //?????????????????????????????????????????????????????????????????????????????????
  //? Variaveis e Estados para filtros controlados
  //?????????????????????????????????????????????????????????????????????????????????
  // Verificar se é filtros controlados:
  const isControlledFiltering = onFiltersChange !== undefined;

  //?????????????????????????????????????????????????????????????????????????????????
  //? Variaveis e Estados para ordenação controlada
  //?????????????????????????????????????????????????????????????????????????????????
  // Verificar se é ordenação controlada:
  const isControlledSorting = sortField !== undefined && sortDirection !== undefined && onSortChange !== undefined;

  //?????????????????????????????????????????????????????????????????????????????????
  //? Variaveis e Estados para paginação controlada (pré table created)
  //?????????????????????????????????????????????????????????????????????????????????
  // Verifica se é paginação controlada (server-side) ou client-side
  const isControlledPagination = totalItems !== undefined && currentPage !== undefined && onPageChange !== undefined;

  // Estado para paginação client-side
  const [pagination, setPagination] = useState({
    pageIndex: 0,
    pageSize: pageSize,
  });

  // Merge das classNames customizadas com as padrões
  const mergedClassNames = {
    ...defaultClassNames,
    ...classNames,
  };

  //?????????????????????????????????????????????????????????????????????????????????
  //? Cria tabela
  //?????????????????????????????????????????????????????????????????????????????????
  const table = useReactTable({
    data,
    columns,
    getCoreRowModel: getCoreRowModel(),
    //!filtros
    getFilteredRowModel: getFilteredRowModel(),
    onColumnFiltersChange: isControlledFiltering
      ? (updater) => {
        // Pega o estado atual dos filtros do servidor
        const currentServerFilters = filters.map((f) => ({ id: f.field, value: f.value }));
        const newFilters = typeof updater === 'function' ? updater(currentServerFilters) : updater;
        const filterArray = newFilters.map((filter) => ({
          field: filter.id,
          value: filter.value,
        }));
        onFiltersChange(filterArray);
        // Volta para a primeira página ao filtrar
        if (onPageChange) {
          onPageChange(1);
        }
      }
      : setColumnFilters,
    //!ordenação
    getSortedRowModel: !isControlledPagination ? getSortedRowModel() : undefined,
    ...(isControlledSorting
      ? {
        manualSorting: true, // Desabilita ordenação automática
      }
      : {}),
    onSortingChange: isControlledSorting
      ? (updater) => {
        const newSorting = typeof updater === 'function' ? updater(sorting) : updater;
        if (newSorting[0]) {
          onSortChange(newSorting[0].id, newSorting[0].desc ? 'desc' : 'asc');
        }
      }
      : setSorting,
    //!paginacao
    getPaginationRowModel: !isControlledPagination ? getPaginationRowModel() : undefined,
    onPaginationChange: !isControlledPagination ? setPagination : undefined,
    state: {
      //!filtros
      columnFilters: isControlledFiltering ? filters.map((f) => ({ id: f.field, value: f.value })) : columnFilters,
      //!ordenacao
      sorting: isControlledSorting ? [{ id: sortField, desc: sortDirection === 'desc' }] : sorting,
      //!paginacao
      ...(!isControlledPagination ? { pagination } : {}),
    },
    ...(isControlledPagination
      ? {
        manualPagination: true,
        pageCount: Math.ceil(totalItems! / pageSize),
      }
      : {}),
  });

  //?????????????????????????????????????????????????????????????????????????????????
  //? Metodos e variaveis para paginação controlada (pós table created)
  //?????????????????????????????????????????????????????????????????????????????????
  const totalPages = isControlledPagination ? Math.ceil(totalItems! / pageSize) : table.getPageCount();
  const startItem = isControlledPagination
    ? (currentPage! - 1) * pageSize + 1
    : pagination.pageIndex * pagination.pageSize + 1;
  const endItem = isControlledPagination
    ? Math.min(currentPage! * pageSize, totalItems!)
    : Math.min((pagination.pageIndex + 1) * pagination.pageSize, table.getFilteredRowModel().rows.length);
  const displayedTotal = isControlledPagination ? totalItems! : table.getFilteredRowModel().rows.length;
  const displayedPage = isControlledPagination ? currentPage! : pagination.pageIndex + 1;
  const currentPageSize = isControlledPagination ? pageSize : pagination.pageSize;

  const handlePageSizeChange = (newSize: number) => {
    if (isControlledPagination && onPageSizeChange) {
      onPageSizeChange(newSize);
      onPageChange!(1); // Volta pra primeira página ao mudar o tamanho
    } else {
      setPagination((prev) => ({
        ...prev,
        pageIndex: 0, // Volta pra primeira página ao mudar o tamanho
        pageSize: newSize,
      }));
    }
  };

  const handleFirstPage = () => {
    if (isControlledPagination && onPageChange) {
      onPageChange(1);
    } else {
      setPagination((prev) => ({
        ...prev,
        pageIndex: 0,
      }));
    }
  };

  const handlePreviousPage = () => {
    if (isControlledPagination && onPageChange && currentPage! > 1) {
      onPageChange(currentPage! - 1);
    } else {
      setPagination((prev) => ({
        ...prev,
        pageIndex: Math.max(0, prev.pageIndex - 1),
      }));
    }
  };

  const handleNextPage = () => {
    if (isControlledPagination && onPageChange && currentPage! < totalPages) {
      onPageChange(currentPage! + 1);
    } else {
      const maxPageIndex = Math.ceil(table.getFilteredRowModel().rows.length / pagination.pageSize) - 1;
      setPagination((prev) => ({
        ...prev,
        pageIndex: Math.min(maxPageIndex, prev.pageIndex + 1),
      }));
    }
  };

  const handleLastPage = () => {
    if (isControlledPagination && onPageChange) {
      onPageChange(totalPages);
    } else {
      const maxPageIndex = Math.ceil(table.getFilteredRowModel().rows.length / pagination.pageSize) - 1;
      setPagination((prev) => ({
        ...prev,
        pageIndex: maxPageIndex,
      }));
    }
  };

  const handleGoToPage = (e: React.KeyboardEvent<HTMLInputElement>) => {
    if (e.key === 'Enter') {
      const pageNumber = parseInt(goToPage);
      if (pageNumber >= 1 && pageNumber <= totalPages) {
        // Se for paginação controlada (server-side)
        if (isControlledPagination && onPageChange) {
          onPageChange(pageNumber);
        } else {
          // Se for paginação client-side
          table.setPageIndex(pageNumber - 1);
        }
      }
      setGoToPage('');
    }
  };

  const canPreviousPage = isControlledPagination ? currentPage! > 1 : pagination.pageIndex > 0;
  const canNextPage = isControlledPagination
    ? currentPage! < totalPages
    : pagination.pageIndex < Math.ceil(table.getFilteredRowModel().rows.length / pagination.pageSize) - 1;

  //?????????????????????????????????????????????????????????????????????????????????
  //? useEffects
  //?????????????????????????????????????????????????????????????????????????????????
  // Reseta a paginação quando os filtros mudam
  useEffect(() => {
    if (!isControlledPagination) {
      // Client-side: reseta o estado local
      setPagination((prev) => ({
        ...prev,
        pageIndex: 0,
      }));
    } else if (onPageChange) {
      // Server-side: chama o callback para voltar à página 1
      onPageChange(1);
    }
  }, [columnFilters, isControlledPagination, onPageChange]);

  return {
    table,
    columns,
    mergedClassNames,
    emptyMessage,
    showPagination,
    isLoading,
    onRowClick,

    // Filtros
    isControlledFiltering,
    filters,

    //Ordencao
    isControlledSorting,
    sortField,
    sortDirection,

    // Paginação
    totalPages,
    startItem,
    endItem,
    displayedTotal,
    displayedPage,
    pageSize: currentPageSize,
    pageSizeOptions,
    handlePageSizeChange,
    handleFirstPage,
    handlePreviousPage,
    handleNextPage,
    handleLastPage,
    canPreviousPage,
    canNextPage,
    handleGoToPage,
    goToPage,
    setGoToPage,
  };
}

type DataTableStatesProps<TData = unknown> = {
  states: ReturnType<typeof useDataTable<TData>>;
};
export type DataTableStates<TData = unknown> = DataTableStatesProps<TData>;
