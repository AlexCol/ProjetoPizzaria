'use client';

import { setAuthFailHandler } from '@/services/api';
import { getAuth } from '@/services/generated/auth/auth';
import { LoginDto, UserSessionPayload } from '@/services/generated/models';
import Logger from '@/utils/Logger';
import { createContext, useContext, useEffect, useState } from 'react';
import { toast } from 'react-toastify';
import { useSseContext } from '../sse/SSEContext';

//*************************************************************
//* Tipagens para o contexto
//*************************************************************
function useAuthProvider() {
  const { registerCommand, unregisterCommand } = useSseContext();
  const [loggedUser, setLoggedUser] = useState<UserSessionPayload | null>(null);
  const [avatarBase64Image, setAvatarBase64Image] = useState<string | undefined>(undefined);
  const [isLoading, setIsLoading] = useState(true);
  const isAuthenticated = !!loggedUser;

  //?????????????????????????????????????????????????????????????????????????????????
  //? Metodos do contexto
  //?????????????????????????????????????????????????????????????????????????????????
  async function signInHandler(data: { email: string; password: string; rememberMe: boolean }) {
    if (!data.email || !data.password || data.rememberMe === null) {
      toast.error('Dados de login inválidos!');
      return;
    }
    await authHandler(data);
  }

  async function signOutHandler() {
    //await signOut();
    setLoggedUser(null);
  }

  //?????????????????????????????????????????????????????????????????????????????????
  //? internal methods
  //?????????????????????????????????????????????????????????????????????????????????
  async function authHandler(params?: LoginDto, setLoad = true) {
    if (setLoad) {
      setIsLoading(true);
    }

    let data: UserSessionPayload | string;
    const controller = getAuth();
    if (params) {
      data = await controller.postApiAuthLogin(params);
    } else {
      data = await controller.getApiAuthSession();
    }

    if (typeof data === 'string') {
      if (data && (loggedUser || params)) {
        //adicionado loggedUser para não aparecer mensagem de erro ao abrir a primeira vez a pagina e não existir ou estiver invalida a sessao
        toast.error(data);
      }
      setLoggedUser(null);
    } else {
      setLoggedUser(data);
    }

    if (setLoad) {
      setIsLoading(false);
    }
  }

  //? metodo para solicitar a imagem do avatar ao servidor
  async function fetchAvatarImage() {

  }

  //? metodo interno para limpar o usuário (que será dado para o serviço 'api' usar - use effect abaixo)
  function invalidateAuth() {
    if (!isAuthenticated) {
      return;
    }
    setLoggedUser(null);
  }

  //?????????????????????????????????????????????????????????????????????????????????
  //? useEffects
  //?????????????????????????????????????????????????????????????????????????????????
  useEffect(() => {
    //!tenta buscar 'me', se cookies ainda forem validos, já vai 'autenticar'
    authHandler();

    //! registra comando SSE para atualização de sessão
    registerCommand('session-update', () => {
      Logger.log('SSE: session-update received, refreshing auth session');
      authHandler(undefined, false);
    });

    return () => {
      //! limpa o comando ao desmontar o componente
      unregisterCommand('session-update');
    };
  }, []);

  useEffect(() => {
    fetchAvatarImage();
  }, [loggedUser]);

  useEffect(() => {
    //! jogar processo de 'desautenticar' o usuário para service 'api' poder usar
    setAuthFailHandler(invalidateAuth);
  }, [invalidateAuth]);

  //?????????????????????????????????????????????????????????????????????????????????
  //? Derivados do estado
  //?????????????????????????????????????????????????????????????????????????????????

  return {
    isAuthenticated,
    isLoading,
    userData: loggedUser,
    signIn: signInHandler,
    signOut: signOutHandler,
    avatarBase64Image,
  };
}
export type AuthContextType = ReturnType<typeof useAuthProvider>;

//*************************************************************
//* Criando o contexto, com base no tipo acima
//*************************************************************
//! mantem privado pra forçar o uso de useAuthContext
const AuthContext = createContext<AuthContextType | undefined>(undefined);

//*************************************************************
//* Componente Provider do contexto (onde são iniciadas as
//* variáveis de estado e as funções que serão passadas no value)
//* E então passadas no value para serem usadas pelos componentes filhos
//*************************************************************
export function AuthProvider({ children }: { children: React.ReactNode }) {
  const auth = useAuthProvider();
  return <AuthContext.Provider value={auth}>{children}</AuthContext.Provider>;
}

//*************************************************************
//* Wrappers para o contexto, de modo que não precise ser chamado
//* useContext([name]Context) diretamente. Mas sim 'use' abaixo
//* que já faz a verificação de undefined e retorna o contexto
//*************************************************************
export function useAuthContext() {
  const context = useContext(AuthContext);
  if (context === undefined) {
    throw new Error('useAuth must be used within an AuthProvider');
  }
  return context;
}
